package BasicProgramDay1;

public class PrintInteger {
    public static void main(String[] args) {
        int c; //declaring integer variable

        /* Using for loop to repeat instruction execution */
        for (c=0;c<=10;c++){
            System.out.println(c);
        }
    }
}

