package JavaTrickyProblem2;

public class StringQuotes {
    public static void main(String[] args) {
        //we want to print string in double quotes like "Hello"
        System.out.println("hello");//it will print hello only
        System.out.println("\"hello\""); // it will print "hello"
        // --> \ before " work like escape character
        System.out.println("I am \"tushar\" \"ashok\" \"dhage\"");
        System.out.println("\'hello\'");
    }
}
